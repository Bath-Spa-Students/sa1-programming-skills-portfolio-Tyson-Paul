#To store information about a person in a dictionary (first name, last name, age, city)
person = {
    "first_name": "Chris",
    "last_name": "Martin",
    "age": 18,
    "city": "Dubai"
}

#To print each information stored in the dictionary
print("First Name:", person["first_name"])
print("Last Name:", person["last_name"])
print("Age:", person["age"])
print("City:", person["city"])